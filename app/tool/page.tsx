export default function ToolPage() {
  const supabase = createClient();
  const router = useRouter();

  const [userEmail, setUserEmail] = useState<string | null>(null);
  const [loading, setLoading] = useState(true);
  const [saving, setSaving] = useState(false);
  const [message, setMessage] = useState("");
  const [selectedTemplate, setSelectedTemplate] = useState<Template>("minimal");

  const [form, setForm] = useState({
    business_name: "",
    client_name: "",
    client_email: "",
    description: "",
    labour_cost: "",
    materials_cost: "",
    vat: false,
  });

  useEffect(() => {
    const checkUser = async () => {
      const { data } = await supabase.auth.getUser();
      if (!data.user) {
        router.push("/auth?mode=login");
        return;
      }
      setUserEmail(data.user.email || null);
      setLoading(false);
    };
    checkUser();
  }, [router, supabase]);

  const labour = parseFloat(form.labour_cost) || 0;
  const materials = parseFloat(form.materials_cost) || 0;
  const subtotal = labour + materials;
  const vatAmount = form.vat ? subtotal * 0.2 : 0;
  const total = subtotal + vatAmount;

  const handleChange = (e: React.ChangeEvent<HTMLInputElement | HTMLTextAreaElement>) => {
    const target = e.target as HTMLInputElement;
    const value = target.type === "checkbox" ? target.checked : target.value;
    setForm((prev) => ({ ...prev, [target.name]: value }));
  };

  const handleSave = async () => {
    setSaving(true);
    setMessage("");
    const { data: userData } = await supabase.auth.getUser();
    if (!userData.user) return;
    const quoteNumber = `Q-${Date.now()}`;
    const { error } = await supabase.from("documents").insert({
      user_id: userData.user.id,
      type: "quote",
      number: quoteNumber,
      client_name: form.client_name,
      client_email: form.client_email,
      description: form.description,
      labour_cost: labour,
      materials_cost: materials,
      vat: form.vat,
      total: total,
      status: "draft",
    });
    setSaving(false);
    if (error) {
      setMessage("Error saving quote: " + error.message);
    } else {
      setMessage("Quote saved successfully.");
      setTimeout(() => router.push("/dashboard"), 1500);
    }
  };

  const handleDownloadPDF = () => {
    const doc = new jsPDF();
    const quoteNumber = `Q-${Date.now()}`;
    const date = new Date().toLocaleDateString("en-GB");

    if (selectedTemplate === "minimal") generateMinimal(doc, form, quoteNumber, date, labour, materials, vatAmount, total);
    if (selectedTemplate === "modern") generateModern(doc, form, quoteNumber, date, labour, materials, vatAmount, total);
    if (selectedTemplate === "formal") generateFormal(doc, form, quoteNumber, date, labour, materials, vatAmount, total);
    if (selectedTemplate === "branded") generateBranded(doc, form, quoteNumber, date, labour, materials, vatAmount, total);

    doc.save(`quote-${quoteNumber}.pdf`);
  };

  if (loading) {
    return (
      <div className="min-h-screen bg-black text-white flex items-center justify-center">
        Loading...
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-black px-6 py-10 text-white">
      <div className="mx-auto max-w-2xl">

        <div className="mb-8 flex items-center justify-between border-b border-green-600/20 pb-4">
          <h1 className="text-3xl font-bold text-green-500">New Quote</h1>
          <span className="text-sm text-zinc-400">{userEmail}</span>
        </div>

        <div className="space-y-4 rounded-2xl border border-zinc-800 bg-zinc-900 p-6">

          <div>
            <label className="mb-1 block text-sm text-zinc-400">Your Business Name</label>
            <input name="business_name" value={form.business_name} onChange={handleChange}
              placeholder="e.g. Smith Plumbing Ltd"
              className="w-full rounded-lg border border-zinc-700 bg-black px-4 py-3 text-white outline-none focus:border-green-500" />
          </div>

          <div>
            <label className="mb-1 block text-sm text-zinc-400">Client Name</label>
            <input name="client_name" value={form.client_name} onChange={handleChange}
              placeholder="e.g. John Davies"
              className="w-full rounded-lg border border-zinc-700 bg-black px-4 py-3 text-white outline-none focus:border-green-500" />
          </div>

          <div>
            <label className="mb-1 block text-sm text-zinc-400">Client Email</label>
            <input name="client_email" value={form.client_email} onChange={handleChange}
              placeholder="e.g. john@example.com"
              className="w-full rounded-lg border border-zinc-700 bg-black px-4 py-3 text-white outline-none focus:border-green-500" />
          </div>

          <div>
            <label className="mb-1 block text-sm text-zinc-400">Job Description</label>
            <textarea name="description" value={form.description} onChange={handleChange}
              placeholder="Describe the work to be done..."
              rows={4}
              className="w-full rounded-lg border border-zinc-700 bg-black px-4 py-3 text-white outline-none focus:border-green-500" />
          </div>

          <div className="grid grid-cols-2 gap-4">
            <div>
              <label className="mb-1 block text-sm text-zinc-400">Labour Cost (£)</label>
              <input name="labour_cost" value={form.labour_cost} onChange={handleChange}
                type="number" placeholder="0.00"
                className="w-full rounded-lg border border-zinc-700 bg-black px-4 py-3 text-white outline-none focus:border-green-500" />
            </div>
            <div>
              <label className="mb-1 block text-sm text-zinc-400">Materials Cost (£)</label>
              <input name="materials_cost" value={form.materials_cost} onChange={handleChange}
                type="number" placeholder="0.00"
                className="w-full rounded-lg border border-zinc-700 bg-black px-4 py-3 text-white outline-none focus:border-green-500" />
            </div>
          </div>

          <div className="flex items-center gap-3">
            <input type="checkbox" name="vat" id="vat" checked={form.vat} onChange={handleChange}
              className="h-4 w-4 accent-green-500" />
            <label htmlFor="vat" className="text-sm text-zinc-400">Add VAT (20%)</label>
          </div>

          <div className="rounded-lg border border-zinc-700 bg-black p-4 space-y-1">
            <div className="flex justify-between text-sm text-zinc-400">
              <span>Subtotal</span><span>£{subtotal.toFixed(2)}</span>
            </div>
            {form.vat && (
              <div className="flex justify-between text-sm text-zinc-400">
                <span>VAT (20%)</span><span>£{vatAmount.toFixed(2)}</span>
              </div>
            )}
            <div className="flex justify-between text-lg font-bold text-white border-t border-zinc-700 pt-2 mt-2">
              <span>Total</span><span>£{total.toFixed(2)}</span>
            </div>
          </div>

          {/* Template Selector */}
          <div>
            <label className="mb-2 block text-sm text-zinc-400">Choose Template</label>
            <div className="grid grid-cols-2 gap-3">
              {TEMPLATES.map((t) => (
                <button
                  key={t.id}
                  onClick={() => setSelectedTemplate(t.id)}
                  className={`rounded-lg border px-4 py-3 text-left transition ${
                    selectedTemplate === t.id
                      ? "border-green-500 bg-green-600/10 text-white"
                      : "border-zinc-700 bg-black text-zinc-400 hover:border-zinc-500"
                  }`}
                >
                  <div className="font-semibold text-sm">{t.name}</div>
                  <div className="text-xs mt-1 opacity-70">{t.desc}</div>
                </button>
              ))}
            </div>
          </div>

          {message && (
            <p className="text-center text-sm text-green-400">{message}</p>
          )}

          <div className="grid grid-cols-2 gap-4">
            <button onClick={handleSave} disabled={saving}
              className="w-full rounded-lg bg-zinc-700 py-3 font-semibold text-white transition hover:bg-zinc-600 disabled:opacity-60">
              {saving ? "Saving..." : "Save Quote"}
            </button>
            <button onClick={handleDownloadPDF}
              className="w-full rounded-lg bg-green-600 py-3 font-semibold text-white transition hover:bg-green-500">
              Download PDF
            </button>
          </div>

        </div>
      </div>
    </div>
  );
}
