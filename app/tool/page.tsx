type Template = "minimal" | "modern" | "formal" | "branded";

const TEMPLATES: { id: Template; name: string; desc: string }[] = [
  { id: "minimal", name: "Clean / Minimal", desc: "Simple, lots of whitespace" },
  { id: "modern", name: "Bold / Modern", desc: "Strong header, clean lines" },
  { id: "formal", name: "Classic / Formal", desc: "Traditional business style" },
  { id: "branded", name: "Branded", desc: "Green accent, your brand colours" },
];

function generateMinimal(doc: jsPDF, form: any, quoteNumber: string, date: string, labour: number, materials: number, vatAmount: number, total: number) {
  doc.setFontSize(10);
  doc.setTextColor(150);
  doc.text(quoteNumber, 196, 20, { align: "right" });
  doc.text(date, 196, 27, { align: "right" });

  doc.setFontSize(24);
  doc.setTextColor(0);
  doc.text(form.business_name || "Your Business", 14, 30);

  doc.setDrawColor(220);
  doc.line(14, 35, 196, 35);

  doc.setFontSize(10);
  doc.setTextColor(100);
  doc.text("PREPARED FOR", 14, 46);
  doc.setTextColor(0);
  doc.setFontSize(12);
  doc.text(form.client_name || "Client Name", 14, 54);
  doc.setFontSize(10);
  doc.setTextColor(120);
  doc.text(form.client_email || "", 14, 61);

  doc.setDrawColor(220);
  doc.line(14, 68, 196, 68);

  doc.setTextColor(100);
  doc.setFontSize(10);
  doc.text("SCOPE OF WORK", 14, 78);
  doc.setTextColor(0);
  doc.setFontSize(11);
  const descLines = doc.splitTextToSize(form.description || "", 170);
  doc.text(descLines, 14, 86);

  const costsY = 86 + descLines.length * 6 + 12;
  doc.setTextColor(100);
  doc.text("Labour", 14, costsY);
  doc.text(`£${labour.toFixed(2)}`, 196, costsY, { align: "right" });
  doc.text("Materials", 14, costsY + 8);
  doc.text(`£${materials.toFixed(2)}`, 196, costsY + 8, { align: "right" });
  if (form.vat) {
    doc.text("VAT (20%)", 14, costsY + 16);
    doc.text(`£${vatAmount.toFixed(2)}`, 196, costsY + 16, { align: "right" });
  }

  const totalY = costsY + (form.vat ? 28 : 20);
  doc.setDrawColor(0);
  doc.line(14, totalY, 196, totalY);
  doc.setFontSize(13);
  doc.setTextColor(0);
  doc.text("Total", 14, totalY + 10);
  doc.text(`£${total.toFixed(2)}`, 196, totalY + 10, { align: "right" });

  doc.setFontSize(8);
  doc.setTextColor(180);
  doc.text("Generated by GetJobQuotes.app", 105, 285, { align: "center" });
}

